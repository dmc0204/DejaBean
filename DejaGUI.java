    private void dejaJumpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dejaJumpActionPerformed

        //Try and Catch for jumping to a selected results URL -dc
        try {
            String url = (String) LinkExtractor.dejaURLExtractResults((String)resultsList.getElementAt(dejaResultsList.getSelectedIndex()));
            Desktop.getDesktop().browse(new URI(url));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Are you sure you have something selected?", "Just checking.", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_dejaJumpActionPerformed

