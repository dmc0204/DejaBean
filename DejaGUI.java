    private void dejaNuzItActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_dejaNuzItActionPerformed

        //stackoverflow reference but the math is basic number line operation -dc
        long testTimeStart = System.currentTimeMillis();

        long timeUrlStart = System.currentTimeMillis();

        //string to catch the input -dc
        String input = new String(dejaTermIn.getText());

        //passing input into the grooming class -dc
        input = Groom.barber(input);

        //object for display -dc
        Display e = new Display();

        //Clearing the list for the new results -dc
        resultsList.clear();
        
        //array used for Collections methods -dc
        TreeSet rankMe = new TreeSet();

        //feeding the chain into the results box -dc
        for (int i = 0; i <= urlNames.getSize() - 1; i++) {

            rankMe.add(e.results(Search.match(input, Parse.getDat(urlList.getModel().getElementAt(i))), urlList.getModel().getElementAt(i)));

            //Logs time in a list for each query from the start of the first event -dc
            long testTimeEnd = System.currentTimeMillis();
            testTimes.addElement((testTimeEnd - timeUrlStart) + "ms @ " + urlList.getModel().getElementAt(i));
            timeUrlStart = System.currentTimeMillis();
        }

        //total time counting results -dc
        long finalEnd = System.currentTimeMillis();
        testTimes.addElement((finalEnd - testTimeStart) + "ms Total Query Time.");

        //for each element k, add to rankMe:TreeSet -dc
        for (int k = 0; k <= resultsList.getSize() - 1; k++) {
            rankMe.add(resultsList.elementAt(k));

        }
        
        //for each object i in rankMe, add element I to resultsList -dc
        for (Object i : rankMe.descendingSet()) {
            resultsList.addElement(i);
        }

    }//GEN-LAST:event_dejaNuzItActionPerformed


